def mostrar_informacion_de(jedi):
    indexJedi = search(jedis, "name", jedi)
    if indexJedi is not None:
        e = jedis[indexJedi]
        print("----------------------------------------------------------------")
        print(f"Informacion del jedi {jedi}:")
        print(f"    -rank: {e['rank']}")
        print(f"    -species: {e['species']}")
        print(f"    -master: {e['master']}")
        print(f"    -lightsaber_color: {e['lightsaber_color']}")
        print(f"    -homeworld: {e['homeworld']}")
        print(f"    -birth_year: {e['birth_year']}")
        print(f"    -height: {e['height']}")
        print(f"    -to_darkside: {e['to_darkside']}")
        print(f"    -come_lightside: {e['come_lightside']}")
        print("----------------------------------------------------------------")

def mostrar_aprendis_de(master):
    aprendises = []
    for jedi in jedis:
        if jedi['master'] != None:
            masters = jedi["master"].split("/")
            print(masters)
        if master in masters:
            aprendises.append(jedi)
        masters = []
    print("----------------------------------------------------------------")
    if aprendises:
        print(f"Aprendis de {master}:")
        for jedi in aprendises:
            print(f"    -{jedi['name']}")
        print("----------------------------------------------------------------")
    else:
        print(f"No hay aprendis de {master}")

    
def mostrar_jedis_especie(especie):
    especie_jedis = []
    for jedi in jedis:
        if jedi['species'] == especie:
            especie_jedis.append(jedi)
    print("----------------------------------------------------------------")
    if especie_jedis:
        print(f"Jedis de la especie {especie}:")
        for jedi in especie_jedis:
            print(f"    -{jedi['name']}")
    else:
        print(f"No hay jedis de la especie {especie}")

def listar_jedi_inicial(inicial):
    jedis_inicial = []
    for jedi in jedis:
        if jedi['name'].startswith(inicial):
            jedis_inicial.append(jedi)
    print("----------------------------------------------------------------")
    if jedis_inicial:
        print(f"Jedis con inicial {inicial}:")
        for jedi in jedis_inicial:
            print(f"    -{jedi['name']}")
    else:
        print(f"No hay jedis con inicial {inicial}")

def listar_jedis_zable_varios_colores():
    jedis_zable_varios_colores = []
    for jedi in jedis:
        if jedi["lightsaber_color"] != None:
            colores = jedi["lightsaber_color"].split("/")
        if len(colores) > 1:
            jedis_zable_varios_colores.append(jedi)
    print("----------------------------------------------------------------")
    if jedis_zable_varios_colores:
        print(f"Jedis con zable varios colores:")
        for jedi in jedis_zable_varios_colores:
            print(f"    -{jedi['name']} -> colores: {jedi['lightsaber_color']}")

def sables_amarillo_o_violeta():
    for jedi in jedis:
        if jedi["lightsaber_color"] != None:
            colores = jedi["lightsaber_color"].split("/")
        if ("violet" in colores) or ("Yellow" in colores):
            print(f"    -{jedi['name']} -> colores: {jedi['lightsaber_color']}")
            
